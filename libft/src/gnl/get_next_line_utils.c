#include "get_next_line.h"

int	ft_found(char *str, char nl)
{
	size_t	i;

	if (!str)
		return (0);
	i = 0;
	while (str[i])
	{
		if (str[i] == nl)
			return (-1);
		i++;
	}
	return (0);
}
